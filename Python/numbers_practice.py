# Question 2: You bought 9 packets of potato chips from a store.
# Each packet costs 1.49 dollar, and you gave shopkeeper 20 dollar.
# Find out how many dollars is the shopkeeper going to give you back?
items = 9
cost_per_item = 1.49
total_given = 20
total_cost = items*cost_per_item
cash_back = total_given-total_cost
print(f"The shopkeepr will return {cash_back}$ to you")


